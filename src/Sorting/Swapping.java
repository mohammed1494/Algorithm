package Sorting;

public class Swapping {
    public static void main(String[] args) {

        int num1 = 10;
        int num2 = 20;
        int temp;
        String ex1 = "Hello";
        String ex2 = "GoodBye";
        String temp1;

        temp1=ex1;
        ex1=ex2;
        ex2=temp1;


        System.out.println(" Temp1 is " + temp1);
        System.out.println(" Ex-1 is " + ex1);
        System.out.println(" Ex-2 is " + ex2);


        temp = num1;
        num1 = num2;
        num2 = temp;

        System.out.println("Num-1 " + num1);
        System.out.println("Num-2 " + num2);
        System.out.println("Temp value is " + temp);





    }





}
